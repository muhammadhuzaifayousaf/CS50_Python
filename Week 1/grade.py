score = int(input("Score: "))

if score >= 85:
    print("Grade: A")
elif score >= 81:
    print("Grade: A-")
elif score >= 75:
    print("Grade: B+")
elif score >= 70:
    print("Grade: B")
elif score >= 67:
    print("Grade: B-")
elif score >= 63:
    print("Grade: C+")
elif score >= 60:
    print("Grade: C")
elif score >= 57:
    print("Grade: C-")
elif score >= 53:
    print("Grade: D+")
elif score >= 50:
    print("Grade: D")
else:
    print("Grade: F")
